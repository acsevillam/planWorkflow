classdef OptimizeStage
    % OptimizeStage Runs reference and robust fluence optimization.

    methods (Static)
        function context = workflowContext(varargin)
            if nargin == 3 && ...
                    planWorkflow.stages.WorkflowRuntime.isRuntime(varargin{3})
                runConfig = varargin{1};
                data = varargin{2};
                runtime = varargin{3};
                taskRunner = runtime.taskRunner();
                logFn = runtime.logFn();
                checkpointFn = runtime.checkpointFn();
            else
                [runConfig,data,taskRunner,logFn] = varargin{1:4};
                if numel(varargin) >= 5
                    checkpointFn = varargin{5};
                else
                    checkpointFn = @(varargin) [];
                end
            end
            context = planWorkflow.stages.OptimizeStage.context( ...
                runConfig,data,taskRunner,logFn,checkpointFn);
        end

        function context = context(runConfig,data,taskRunner,logFn, ...
                checkpointFn)
            if nargin < 5 || isempty(checkpointFn)
                checkpointFn = @(varargin) [];
            end
            data = planWorkflow.stages.ContextValidator.dataSlice( ...
                data,{'optimizationInput'}, ...
                {'ct','cst','stf','pln','dosePulling', ...
                'robustPlans','objectiveInfo','quantityOpt'}, ...
                'optimize');
            context = planWorkflow.stages.ContextValidator.base( ...
                'optimize',runConfig,data,taskRunner,logFn);
            context.checkpoint = checkpointFn;
        end

        function patch = run(context)
            data = context.data;
            logFn = context.log;
            analysis = planWorkflow.analysis.AnalysisService.completeConfig( ...
                context.runConfig,data);
            context.runConfig.analysis = analysis;

            reference = ...
                planWorkflow.config.RobustPlanConfig.referenceFromRunConfig( ...
                context.runConfig);
            referenceLabel = ...
                planWorkflow.results.PlanLabels.referencePlanDisplayLabel( ...
                reference);
            cachePath = ...
                planWorkflow.resources.StageDataLifecycle.cachePath( ...
                context.runConfig);
            referenceUnitInfo = ...
                planWorkflow.stages.OptimizeStage.referenceUnitInfo( ...
                referenceLabel);
            referenceInputLight = ...
                planWorkflow.precompute.OptimizationInput.requireLight( ...
                data,'reference optimization');
            referenceInitialWeights = ...
                planWorkflow.stages.OptimizeStage.referenceInitialWeights( ...
                data);
            [resultGUIReference,reusedReference] = ...
                planWorkflow.stages.OptimizeStage.reusableReferenceResult( ...
                data,context.runConfig,cachePath,referenceUnitInfo, ...
                referenceInputLight);
            if reusedReference
                logFn(sprintf('Reusing completed %s optimization.', ...
                    referenceLabel));
                referenceInput = referenceInputLight;
            else
                logFn(sprintf('Running %s optimization.',referenceLabel));
                referenceInput = ...
                    planWorkflow.precompute.OptimizationInput.requireFullDij( ...
                    data,'reference optimization',context.runConfig, ...
                    cachePath,data,logFn);
                if ~planWorkflow.precompute.OptimizationInput.isNominal( ...
                        referenceInput)
                    referenceInput.pln = ...
                        planWorkflow.optimization.PlanOptimizationService.apply4DConfig( ...
                        referenceInput.pln,reference);
                end
                resultGUIReference = context.runMeasuredPlanTask( ...
                    'optimize','reference',referenceLabel, ...
                    'fluenceOptimization','','', ...
                    @() planWorkflow.optimization.PlanOptimizationService.runFluenceOptimization( ...
                    context.runConfig,referenceInput.dij,referenceInput.cst, ...
                    referenceInput.pln, ...
                    referenceInitialWeights));
                resultGUIReference = ...
                    planWorkflow.results.ResultGUICompactor.compact( ...
                    resultGUIReference);
                resultGUIReference = ...
                    planWorkflow.persistence.OptimizationResultArtifact.persistResult( ...
                    resultGUIReference,context.runConfig,cachePath, ...
                    referenceUnitInfo.unitKey,referenceUnitInfo, ...
                    referenceInputLight);
            end

            data.optimizationInput = referenceInput;
            data.ct = referenceInput.ct;
            data.cst = referenceInput.cst;
            data.stf = referenceInput.stf;
            data.pln = referenceInput.pln;
            data.resultGUIReference = resultGUIReference;
            data.resultGUI = resultGUIReference;

            if planWorkflow.stages.OptimizeStage.hasRobustDataPlans(data)
                data = ...
                    planWorkflow.stages.OptimizeStage.compactDoseInfluenceArtifacts( ...
                    data,context.runConfig,logFn, ...
                    'reference optimization');
                planWorkflow.stages.OptimizeStage.checkpoint( ...
                    context,data,'reference optimization');
                referenceInput = [];
                for robustPlanIx = 1:numel(data.robustPlans)
                    robustData = data.robustPlans{robustPlanIx};
                    logFn(sprintf(['Running robust optimization plan "%s" ' ...
                        'with strategy %s.'], ...
                        char(robustData.planConfig.label), ...
                        robustData.strategy.name));
                    optimizationInputLight = ...
                        planWorkflow.precompute.OptimizationInput.requireLight( ...
                        robustData,['robust optimization plan "' ...
                        char(robustData.planConfig.label) '"']);
                    numVariants = ...
                        planWorkflow.config.RobustPlanConfig.variantWithPenaltyCount( ...
                        robustData.planConfig);
                    robustData.variantResults = ...
                        planWorkflow.stages.OptimizeStage.existingVariantResults( ...
                        robustData);
                    [robustData,allReused] = ...
                        planWorkflow.stages.OptimizeStage.reuseCompletedVariantResults( ...
                        robustData,robustPlanIx,numVariants, ...
                        optimizationInputLight,context.runConfig,cachePath, ...
                        logFn);
                    if allReused
                        data.robustPlans{robustPlanIx} = robustData;
                        planWorkflow.stages.OptimizeStage.checkpointRobustPlan( ...
                            context,data,robustData,robustPlanIx, ...
                            ['robust optimization plan "' ...
                            char(robustData.planConfig.label) '"']);
                        continue;
                    end
                    optimizationInput = ...
                        planWorkflow.precompute.OptimizationInput.requireFullDij( ...
                        robustData,['robust optimization plan "' ...
                        char(robustData.planConfig.label) '"'], ...
                        context.runConfig, ...
                        cachePath, ...
                        data,logFn);
                    robustData.optimizationInput = optimizationInput;

                    for variantIx = 1:numVariants
                        variantConfig = ...
                            planWorkflow.config.RobustPlanConfig.variantWithPenalty( ...
                            robustData.planConfig,variantIx);
                        variantLabel = ...
                            planWorkflow.results.PlanLabels.robustResultLabel( ...
                            robustData.planConfig,variantIx);
                        unitInfo = ...
                            planWorkflow.stages.OptimizeStage.robustUnitInfo( ...
                            robustData,robustPlanIx,variantIx, ...
                            variantConfig,variantLabel);
                        [resultGUI,reusedVariant] = ...
                            planWorkflow.stages.OptimizeStage.reusableVariantResult( ...
                            robustData,variantIx,context.runConfig,cachePath, ...
                            unitInfo,optimizationInputLight);
                        if reusedVariant
                            logFn(sprintf(['Reusing completed robust ' ...
                                'optimization result "%s".'],variantLabel));
                            robustData.variantResults(variantIx) = ...
                                planWorkflow.results.VariantResults.create( ...
                                robustData,variantIx,resultGUI);
                            continue;
                        end
                        initialWeights = ...
                            planWorkflow.stages.OptimizeStage.initialWeightsForVariant( ...
                            robustData,variantIx);
                        planForOptimization = ...
                            planWorkflow.optimization.VariantPlanFactory.build( ...
                            robustData,variantIx);
                        cstForOptimization = ...
                            planWorkflow.precompute.RobustDataFactory.cstForVariant( ...
                            robustData,variantIx);
                        resultGUI = ...
                            context.runMeasuredPlanTask( ...
                            'optimize','robust', ...
                            variantLabel, ...
                            'fluenceOptimization', ...
                            char(robustData.planConfig.id), ...
                            char(variantConfig.id), ...
                            @() planWorkflow.optimization.PlanOptimizationService.runFluenceOptimization( ...
                            context.runConfig, ...
                            optimizationInput.dij, ...
                            cstForOptimization, ...
                            planForOptimization,initialWeights));
                        resultGUI = ...
                            planWorkflow.results.ResultGUICompactor.compact( ...
                            resultGUI);
                        resultGUI = ...
                            planWorkflow.persistence.OptimizationResultArtifact.persistResult( ...
                            resultGUI,context.runConfig,cachePath, ...
                            unitInfo.unitKey,unitInfo,optimizationInputLight);
                        robustData.variantResults(variantIx) = ...
                            planWorkflow.results.VariantResults.create( ...
                            robustData,variantIx,resultGUI);
                        planWorkflow.stages.OptimizeStage.checkpointRobustPlan( ...
                            context,data,robustData,robustPlanIx, ...
                            ['robust optimization result "' variantLabel '"']);
                    end

                    data.robustPlans{robustPlanIx} = ...
                        planWorkflow.stages.OptimizeStage.compactRobustPlan( ...
                        robustData,data,context.runConfig);
                    optimizationInput = [];
                    planWorkflow.stages.OptimizeStage.logCompactedPlan( ...
                        logFn,['robust optimization plan "' ...
                        char(robustData.planConfig.label) '"']);
                    robustData = [];
                end
            end

            patch = struct();
            patch.runConfig = struct('analysis',analysis);
            patch.data = struct();
            patch.data.optimizationInput = data.optimizationInput;
            patch.data.ct = data.ct;
            patch.data.cst = data.cst;
            patch.data.stf = data.stf;
            patch.data.pln = data.pln;
            patch.data.resultGUIReference = data.resultGUIReference;
            patch.data.resultGUI = data.resultGUI;
            if isfield(data,'robustPlans')
                patch.data.robustPlans = data.robustPlans;
            end
        end
    end

    methods (Static, Access = private)
        function unitInfo = referenceUnitInfo(referenceLabel)
            unitInfo = struct( ...
                'role','reference', ...
                'planId','reference', ...
                'variantId','', ...
                'label',char(referenceLabel), ...
                'planIndex',0, ...
                'variantIndex',0, ...
                'unitKey','reference_000_reference');
            unitInfo = ...
                planWorkflow.persistence.OptimizationResultArtifact.normalizeUnitInfo( ...
                unitInfo);
        end

        function unitInfo = robustUnitInfo(robustData,robustPlanIx, ...
                variantIx,variantConfig,variantLabel)
            unitInfo = struct( ...
                'role','robust', ...
                'planId',char(robustData.planConfig.id), ...
                'variantId',char(variantConfig.id), ...
                'label',char(variantLabel), ...
                'planIndex',robustPlanIx, ...
                'variantIndex',variantIx, ...
                'unitKey',sprintf('robust_%03d_%s_%03d_%s', ...
                robustPlanIx, ...
                planWorkflow.stages.OptimizeStage.safeName( ...
                robustData.planConfig.id),variantIx, ...
                planWorkflow.stages.OptimizeStage.safeName( ...
                variantConfig.id)));
            unitInfo = ...
                planWorkflow.persistence.OptimizationResultArtifact.normalizeUnitInfo( ...
                unitInfo);
        end

        function [resultGUI,reused] = reusableReferenceResult( ...
                data,runConfig,cachePath,unitInfo,optimizationInput)
            resultGUI = struct();
            reused = false;
            if isfield(data,'resultGUIReference') && ...
                    planWorkflow.stages.OptimizeStage.isResultUsable( ...
                    optimizationInput,data.resultGUIReference,'reference','', ...
                    'reference optimization')
                resultGUI = ...
                    planWorkflow.persistence.OptimizationResultArtifact.persistResult( ...
                    data.resultGUIReference,runConfig,cachePath, ...
                    unitInfo.unitKey,unitInfo,optimizationInput);
                reused = true;
                return;
            end
            [resultGUI,reused] = ...
                planWorkflow.persistence.OptimizationResultArtifact.cachedResult( ...
                runConfig,cachePath,unitInfo.unitKey,unitInfo, ...
                optimizationInput);
        end

        function [robustData,allReused] = reuseCompletedVariantResults( ...
                robustData,robustPlanIx,numVariants,optimizationInput, ...
                runConfig,cachePath,logFn)
            allReused = true;
            for variantIx = 1:numVariants
                variantConfig = ...
                    planWorkflow.config.RobustPlanConfig.variantWithPenalty( ...
                    robustData.planConfig,variantIx);
                variantLabel = ...
                    planWorkflow.results.PlanLabels.robustResultLabel( ...
                    robustData.planConfig,variantIx);
                unitInfo = ...
                    planWorkflow.stages.OptimizeStage.robustUnitInfo( ...
                    robustData,robustPlanIx,variantIx,variantConfig, ...
                    variantLabel);
                [resultGUI,reused] = ...
                    planWorkflow.stages.OptimizeStage.reusableVariantResult( ...
                    robustData,variantIx,runConfig,cachePath,unitInfo, ...
                    optimizationInput);
                if reused
                    robustData.variantResults(variantIx) = ...
                        planWorkflow.results.VariantResults.create( ...
                        robustData,variantIx,resultGUI);
                    planWorkflow.stages.OptimizeStage.log( ...
                        logFn,sprintf(['Reusing completed robust ' ...
                        'optimization result "%s".'],variantLabel));
                else
                    allReused = false;
                end
            end
        end

        function [resultGUI,reused] = reusableVariantResult( ...
                robustData,variantIx,runConfig,cachePath,unitInfo, ...
                optimizationInput)
            resultGUI = struct();
            reused = false;
            if numel(robustData.variantResults) >= variantIx
                variantResult = robustData.variantResults(variantIx);
                if planWorkflow.stages.OptimizeStage.isVariantResultUsable( ...
                        robustData,variantResult,variantIx,optimizationInput)
                    resultGUI = ...
                        planWorkflow.persistence.OptimizationResultArtifact.persistResult( ...
                        variantResult.resultGUI,runConfig,cachePath, ...
                        unitInfo.unitKey,unitInfo,optimizationInput);
                    reused = true;
                    return;
                end
            end
            [resultGUI,reused] = ...
                planWorkflow.persistence.OptimizationResultArtifact.cachedResult( ...
                runConfig,cachePath,unitInfo.unitKey,unitInfo, ...
                optimizationInput);
        end

        function tf = isVariantResultUsable(robustData,variantResult, ...
                variantIx,optimizationInput)
            tf = false;
            if ~isstruct(variantResult) || ...
                    ~isfield(variantResult,'resultGUI') || ...
                    isempty(variantResult.resultGUI)
                return;
            end
            variantConfig = ...
                planWorkflow.config.RobustPlanConfig.variantWithPenalty( ...
                robustData.planConfig,variantIx);
            if ~isfield(variantResult,'planId') || ...
                    ~strcmp(char(variantResult.planId), ...
                    char(robustData.planConfig.id)) || ...
                    ~isfield(variantResult,'variantId') || ...
                    ~strcmp(char(variantResult.variantId), ...
                    char(variantConfig.id))
                return;
            end
            tf = planWorkflow.stages.OptimizeStage.isResultUsable( ...
                optimizationInput,variantResult.resultGUI, ...
                robustData.planConfig.id,variantConfig.id, ...
                'robust optimization resume');
        end

        function tf = isResultUsable(optimizationInput,resultGUI,planId, ...
                variantId,purpose)
            tf = false;
            try
                planWorkflow.precompute.OptimizationInput.assertWeightSteeringMatch( ...
                    optimizationInput,resultGUI,planId,variantId,purpose);
                tf = true;
            catch
                tf = false;
            end
        end

        function results = existingVariantResults(robustData)
            results = planWorkflow.results.VariantResults.empty(0);
            if isstruct(robustData) && isfield(robustData,'variantResults') && ...
                    ~isempty(robustData.variantResults)
                results = robustData.variantResults;
            end
        end

        function checkpoint(context,data,label)
            if ~isfield(context,'checkpoint') || isempty(context.checkpoint)
                return;
            end
            patch = struct('data',data);
            try
                context.checkpoint('optimize',patch,label);
            catch ME
                context.log(sprintf( ...
                    'Could not save optimization checkpoint after %s: %s', ...
                    char(label),ME.message));
            end
        end

        function checkpointRobustPlan(context,data,robustData, ...
                robustPlanIx,label)
            checkpointData = data;
            checkpointData.robustPlans{robustPlanIx} = ...
                planWorkflow.stages.OptimizeStage.compactRobustPlan( ...
                robustData,data,context.runConfig);
            planWorkflow.stages.OptimizeStage.checkpoint( ...
                context,checkpointData,label);
        end

        function robustData = compactRobustPlan(robustData,data,runConfig)
            cachePath = ...
                planWorkflow.resources.StageDataLifecycle.cachePath(runConfig);
            if isempty(cachePath)
                return;
            end
            compactData = data;
            compactData.robustPlans = {robustData};
            compactData = ...
                planWorkflow.persistence.WorkflowDataArtifact.compactForStageBoundary( ...
                compactData,runConfig,cachePath);
            robustData = compactData.robustPlans{1};
        end

        function logCompactedPlan(logFn,contextLabel)
            planWorkflow.stages.OptimizeStage.log( ...
                logFn,sprintf(['Compacted dose-influence artifacts after ' ...
                '%s.'],char(contextLabel)));
        end

        function log(logFn,message)
            if isempty(logFn)
                return;
            end
            try
                logFn(message);
            catch
            end
        end

        function value = safeName(value)
            value = regexprep(char(value),'[^A-Za-z0-9_.-]','_');
            if isempty(value)
                value = 'unnamed';
            end
        end

        function tf = hasRobustDataPlans(data)
            tf = isfield(data,'robustPlans') && ~isempty(data.robustPlans);
        end

        function weights = referenceInitialWeights(data)
            weights = [];
            if isfield(data,'dosePulling') && ...
                    isfield(data.dosePulling,'reference') && ...
                    isfield(data.dosePulling.reference,'initialWeights')
                weights = data.dosePulling.reference.initialWeights;
            end
        end

        function weights = initialWeightsForVariant(robustData,variantIx)
            weights = [];
            if isfield(robustData,'initialWeights') && ...
                    numel(robustData.initialWeights) >= variantIx
                weights = robustData.initialWeights{variantIx};
            elseif variantIx > 1
                previousResults = ...
                    planWorkflow.results.VariantResults.optional( ...
                    robustData);
                if numel(previousResults) >= variantIx - 1 && ...
                        isfield(previousResults(variantIx - 1).resultGUI,'w')
                    weights = previousResults(variantIx - 1).resultGUI.w;
                end
            end
        end

        function data = compactDoseInfluenceArtifacts( ...
                data,runConfig,logFn,contextLabel)
            cachePath = ...
                planWorkflow.resources.StageDataLifecycle.cachePath( ...
                runConfig);
            if isempty(cachePath)
                return;
            end
            data = ...
                planWorkflow.persistence.WorkflowDataArtifact.compactForStageBoundary( ...
                data,runConfig,cachePath);
            if nargin >= 3 && ~isempty(logFn)
                try
                    logFn(sprintf(['Compacted dose-influence artifacts ' ...
                        'after %s.'],char(contextLabel)));
                catch
                end
            end
        end
    end
end
