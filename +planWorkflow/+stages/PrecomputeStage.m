classdef PrecomputeStage
    % PrecomputeStage Owns reference and robust dose influence precompute.

    methods (Static)
        function context = workflowContext(varargin)
            if nargin == 3 && ...
                    planWorkflow.stages.WorkflowRuntime.isRuntime(varargin{3})
                runConfig = varargin{1};
                data = varargin{2};
                runtime = varargin{3};
                taskRunner = runtime.taskRunner();
                logFn = runtime.logFn();
                planTemplate = ...
                    planWorkflow.stages.ContextValidator.planTemplate( ...
                    runConfig,data);
                cachePath = char(runConfig.cacheRootPath);
            else
                [runConfig,data,taskRunner,logFn,planTemplate, ...
                    cachePath] = varargin{:};
            end
            cache = planWorkflow.cache.DoseInfluenceCacheService( ...
                runConfig,cachePath,logFn);
            context = planWorkflow.stages.PrecomputeStage.context( ...
                runConfig,data,taskRunner,logFn,planTemplate,cachePath, ...
                cache);
        end

        function context = context(runConfig,data,taskRunner,logFn, ...
                planTemplate,cachePath,cache)
            data = planWorkflow.stages.ContextValidator.dataSlice( ...
                data,{'ct','cst','pln','stf'}, ...
                {'quantityOpt','objectiveInfo','planTemplate', ...
                'planTemplateHash'},'precompute');
            context = planWorkflow.stages.ContextValidator.base( ...
                'precompute',runConfig,data,taskRunner,logFn);
            context.planTemplate = planTemplate;
            context.cachePath = cachePath;
            context.cache = cache;
            planWorkflow.stages.ContextValidator.requireFields( ...
                context,{'planTemplate','cachePath','cache'},'precompute');
            planWorkflow.stages.ContextValidator.requireObjectMethods( ...
                cache,{'getOrCreate','getOrCalculateTransient','file', ...
                'metadata','isIdentityCompatible','stfSignature', ...
                'ensureFileFolder'},'precompute.cache');
        end

        function patch = run(context)
            data = context.data;
            runConfig = context.runConfig;
            logFn = context.log;

            reference = ...
                planWorkflow.config.RobustPlanConfig.referenceFromRunConfig( ...
                runConfig);
            referenceLabel = ...
                planWorkflow.results.PlanLabels.referencePlanDisplayLabel( ...
                reference);
            logFn(sprintf('Precomputing %s dose influence matrix.', ...
                referenceLabel));
            data.pln = planWorkflow.plan.Plan.applyDoseResolution( ...
                runConfig,data.pln);
            data.pln.multScen = ...
                planWorkflow.precompute.PrepareService.referenceScenarioModel( ...
                runConfig,data);
            data.pln = ...
                planWorkflow.optimization.PlanOptimizationService.apply4DConfig( ...
                data.pln,reference);
            data.dij = context.runMeasuredPlanTask( ...
                'precompute','reference',referenceLabel,'doseInfluence', ...
                '','',@() context.cache.getOrCreate( ...
                'reference',data.ct,data.cst,data.stf,data.pln));

            robustPlanConfigs = ...
                planWorkflow.config.RobustPlanConfig.plansFromRunConfig( ...
                runConfig);
            if ~isempty(robustPlanConfigs)
                data.robustPlans = cell(1,numel(robustPlanConfigs));
            end

            for robustPlanIx = 1:numel(robustPlanConfigs)
                robustPlanConfig = robustPlanConfigs(robustPlanIx);
                robustStrategy = ...
                    planWorkflow.robustness.AbstractStrategy.create( ...
                    robustPlanConfig.strategy);
                logFn(sprintf(['Preparing robust plan "%s" with ' ...
                    'strategy %s.'],char(robustPlanConfig.label), ...
                    robustStrategy.name));

                robustPlanLabel = char(robustPlanConfig.label);
                robustPlanId = char(robustPlanConfig.id);
                robustData = context.runMeasuredPlanTask( ...
                    'precompute','robust',robustPlanLabel, ...
                    'prepareRobustData',robustPlanId,'', ...
                    @() planWorkflow.precompute.RobustDataFactory.build( ...
                    runConfig,context.planTemplate,robustPlanConfig,data));
                robustData = context.runMeasuredPlanTask( ...
                    'precompute','robust',robustPlanLabel, ...
                    'applyRobustness',robustPlanId,'', ...
                    @() planWorkflow.precompute.RobustDataFactory.applyFirstVariant( ...
                    runConfig,robustData));

                if robustStrategy.requiresIntervalDij()
                    [cacheHit,robustData] = ...
                        context.runMeasuredPlanTask( ...
                        'precompute','robust',robustPlanLabel, ...
                        'intervalDoseInfluenceCacheRead', ...
                        robustPlanId,'', ...
                        @() planWorkflow.precompute.IntervalDoseInfluence.loadCached( ...
                        context,robustData));
                    if cacheHit
                        robustData = ...
                            planWorkflow.precompute.IntervalDoseInfluence.useForOptimization( ...
                            robustData);
                        logFn(sprintf(['Using dij_interval for %s ' ...
                            'optimization; robust scenario dij is not kept ' ...
                            'in memory.'],robustData.strategy.name));
                        data.robustPlans{robustPlanIx} = robustData;
                        continue;
                    end
                else
                    cacheHit = false;
                end

                logFn(sprintf(['Precomputing robust dose influence ' ...
                    'matrix for plan "%s" (%s).'], ...
                    char(robustPlanConfig.label),robustStrategy.name));
                robustData.dij = context.runMeasuredPlanTask( ...
                    'precompute','robust',robustPlanLabel, ...
                    'robustDoseInfluence',robustPlanId,'', ...
                    @() planWorkflow.stages.PrecomputeStage.getRobustDoseInfluence( ...
                    context,robustData));

                if robustStrategy.requiresIntervalDij() && ~cacheHit
                    robustData = context.runMeasuredPlanTask( ...
                        'precompute','robust',robustPlanLabel, ...
                        'intervalDoseInfluence',robustPlanId,'', ...
                        @() planWorkflow.precompute.IntervalDoseInfluence.precompute( ...
                        context,robustData));
                    robustData = ...
                        planWorkflow.precompute.IntervalDoseInfluence.useForOptimization( ...
                        robustData);
                    logFn(sprintf(['Using dij_interval for %s ' ...
                        'optimization; robust scenario dij is not kept ' ...
                        'in memory.'],robustData.strategy.name));
                end

                data.robustPlans{robustPlanIx} = robustData;
            end

            patch = struct();
            patch.data = struct();
            patch.data.pln = data.pln;
            patch.data.dij = data.dij;
            if isfield(data,'robustPlans')
                patch.data.robustPlans = data.robustPlans;
            end
        end
    end

    methods (Static, Access = private)
        function dij = getRobustDoseInfluence(context,robustData)
            tag = ['robust_' char(robustData.planConfig.id)];
            if robustData.strategy.requiresIntervalDij()
                dij = context.cache.getOrCalculateTransient( ...
                    tag,robustData,robustData.ct,robustData.cst, ...
                    robustData.stf,robustData.pln);
            else
                dij = context.cache.getOrCreate( ...
                    tag,robustData.ct,robustData.cst,robustData.stf, ...
                    robustData.pln);
            end
        end
    end
end
