    rmpath(folder);
end
clear('matRad_samplingAnalysis','matRad_convertToEvaluationMode', ...
    'matRad_showDVHFromSampling','matRad_plotSamplingDoseCubeAnalysis', ...
    'matRad_plotSamplingRobustnessAnalysis', ...
