end

function closeFiguresIfOpen(figures)
for i = 1:numel(figures)
    if ishghandle(figures(i))
        close(figures(i));
    end
end
end

function titleText = readFigureTitle(filePath)
fig = openfig(filePath,'invisible');
cleanup = onCleanup(@() close(fig));
axesHandles = findall(fig,'Type','Axes');
axesHandles = axesHandles(~strcmp(get(axesHandles,'Tag'),'Colorbar'));
titleText = get(get(findTitledAxes(axesHandles),'Title'),'String');
clear cleanup;
end

function fig = newHiddenFigureWithTitle(titleText)
fig = figure('Visible','off');
axesHandle = axes('Parent',fig);
title(axesHandle,titleText);
end

