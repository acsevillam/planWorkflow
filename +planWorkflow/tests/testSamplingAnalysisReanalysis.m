function verifyStaticFigureHasNoSliceControl(testCase,filePath)
fig = openfig(filePath,'invisible');
cleanup = onCleanup(@() close(fig)); %#ok<NASGU>
verifyFalse(testCase,isappdata(fig,'planWorkflowSlicePayload'));
verifyEmpty(testCase,findobj(fig,'Tag','planWorkflowSliceSlider'));
end

function verifyInteractiveSliceFigure(testCase,filePath,kind,sliceMax)
fig = openfig(filePath,'invisible');
cleanup = onCleanup(@() close(fig)); %#ok<NASGU>
verifyTrue(testCase,isappdata(fig,'planWorkflowSlicePayload'));
payload = getappdata(fig,'planWorkflowSlicePayload');
verifyEqual(testCase,payload.kind,kind);
slider = findobj(fig,'Tag','planWorkflowSliceSlider');
verifyNotEmpty(testCase,slider);
verifyEqual(testCase,get(slider,'Min'),1);
verifyEqual(testCase,get(slider,'Max'),sliceMax);
set(slider,'Value',sliceMax);
planWorkflow.analysis.Figures.updateSliceFigure(slider,[]);
payload = getappdata(fig,'planWorkflowSlicePayload');
verifyEqual(testCase,payload.slice,sliceMax);
axesHandle = findTitledAxes(mainAxesInFigure(fig));
userData = get(axesHandle,'UserData');
verifyEqual(testCase,userData.slice,sliceMax);
end

function axesHandles = mainAxesInFigure(fig)
axesHandles = findall(fig,'Type','Axes');
tags = get(axesHandles,'Tag');
if ischar(tags)
    tags = {tags};
end
axesHandles = axesHandles(~strcmp(tags,'Colorbar'));
end

function axesHandle = findTitledAxes(axesHandles)
axesHandle = axesHandles(1);
for i = 1:numel(axesHandles)
    candidateTitle = get(get(axesHandles(i),'Title'),'String');
    if ~isempty(candidateTitle)
        axesHandle = axesHandles(i);
        return;
    end
end
end
