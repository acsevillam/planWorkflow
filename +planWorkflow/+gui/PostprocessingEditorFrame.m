classdef PostprocessingEditorFrame
    % PostprocessingEditorFrame Owns the postprocessing editor widgets.

    methods (Static)
        function frame = create(options,callbacks)
            if nargin < 1
                options = struct();
            end
            if nargin < 2
                callbacks = struct();
            end
            fig = figure('Name','Plan workflow postprocessing', ...
                'NumberTitle','off','MenuBar','none','ToolBar','none', ...
                'WindowStyle',planWorkflow.gui.EditorChrome.editorWindowStyle(), ...
                'Color',[0.94 0.94 0.94], ...
                'Position',[120 90 1120 760], ...
                'CloseRequestFcn', ...
                planWorkflow.gui.PostprocessingEditorFrame.callback(callbacks,'close'));

            tabGroup = uitabgroup('Parent',fig,'Units','normalized', ...
                'Position',[0.02 0.20 0.96 0.76]);
            frame.dataTab = uitab(tabGroup,'Title','Data');
            frame.parametersTab = uitab(tabGroup,'Title','Parameters');
            frame.plotsTab = frame.parametersTab;
            frame.filtersTab = uitab(tabGroup,'Title','Filters');
            frame.outputTab = uitab(tabGroup,'Title','Output');
            frame.resultsTab = [];
            frame.resultGroup = [];

            frame = planWorkflow.gui.PostprocessingEditorFrame.addDataTab( ...
                frame,callbacks);
            frame = planWorkflow.gui.PostprocessingEditorFrame.addParametersTab( ...
                frame,callbacks);
            frame = planWorkflow.gui.PostprocessingEditorFrame.addFiltersTab( ...
                frame,callbacks);
            frame = planWorkflow.gui.PostprocessingEditorFrame.addOutputTab( ...
                frame,callbacks);
            frame = planWorkflow.gui.PostprocessingEditorFrame.addFooter( ...
                frame,fig,tabGroup,callbacks);

            frame.fig = fig;
            frame.tabGroup = tabGroup;

            if isfield(options,'outputDir') && ~isempty(options.outputDir)
                set(frame.outputDirEdit,'String',char(options.outputDir));
            end
            if isfield(options,'pythonPath') && ~isempty(options.pythonPath)
                set(frame.pythonPathEdit,'String',char(options.pythonPath));
            end
        end

        function frame = showResultsTab(frame,outputDir,entries,callbacks)
            frame = planWorkflow.gui.PostprocessingEditorFrame.deleteResultsTab( ...
                frame);
            frame.resultsTab = uitab(frame.tabGroup,'Title','Results');
            frame.resultGroup = uitabgroup('Parent',frame.resultsTab, ...
                'Units','normalized','Position',[0.02 0.10 0.96 0.87]);
            frame = planWorkflow.gui.PostprocessingEditorFrame.addFiguresPanelTab( ...
                frame,frame.resultGroup,char(outputDir),entries,callbacks);
            try
                set(frame.tabGroup,'SelectedTab',frame.resultsTab);
            catch
            end
        end

        function frame = deleteResultsTab(frame)
            if isfield(frame,'resultsTab') && ~isempty(frame.resultsTab) && ...
                    ishandle(frame.resultsTab)
                delete(frame.resultsTab);
            end
            frame.resultsTab = [];
            frame.resultGroup = [];
        end
    end

