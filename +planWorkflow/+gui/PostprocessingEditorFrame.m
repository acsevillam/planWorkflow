        function frame = addFiguresPanelTab(frame,resultGroup,outputDir, ...
                entries,callbacks)
            tab = uitab(resultGroup,'Title','Figures');
            panel = planWorkflow.gui.PostprocessingEditorFrame.createReadOnlyPanel( ...
                tab,'planWorkflowPostprocessingFiguresPanel');

            rowControls = gobjects(0);
            y = 0.93;
            [controls,rowHeight] = ...
                planWorkflow.gui.PostprocessingEditorFrame.addPanelRow( ...
                panel,1,y,'Folder',outputDir,'Open', ...
                @(src,event) planWorkflow.gui.PostprocessingEditorFrame.openOutputFolder( ...
                callbacks,src,event,outputDir),'');
            rowControls = [rowControls controls(:)'];
            y = y - planWorkflow.gui.PostprocessingEditorFrame.panelRowStride( ...
                rowHeight);

            for i = 1:numel(entries)
                [~,name,ext] = fileparts(entries(i).filePath);
                [controls,rowHeight] = ...
                    planWorkflow.gui.PostprocessingEditorFrame.addPanelRow( ...
                    panel,i + 1,y,entries(i).label,[name ext],'Open', ...
                    @(src,event) planWorkflow.gui.PostprocessingEditorFrame.openResultFile( ...
                    callbacks,src,event,entries(i).filePath),'');
                rowControls = [rowControls controls(:)']; %#ok<AGROW>
                y = y - ...
                    planWorkflow.gui.PostprocessingEditorFrame.panelRowStride( ...
                    rowHeight);
            end

            scrollSlider = planWorkflow.gui.PanelScroller.panelSlider(panel);
            if ~isempty(scrollSlider)
                planWorkflow.gui.PanelScroller.configure( ...
                    scrollSlider,rowControls,y);
            end
        end

